part of 'theme_data.dart';

/// Represents theme spacing.
class ThemeSpacing {
  /// Creates a new [ThemeSpacing].
  const ThemeSpacing({
    required this.xs,
    required this.sm,
    required this.md,
    required this.lg,
    required this.xl,
  });

  /// Extra small spacing.
  final double xs;

  /// Small spacing.
  final double sm;

  /// Medium spacing.
  final double md;

  /// Large spacing.
  final double lg;

  /// Extra large spacing.
  final double xl;
}
